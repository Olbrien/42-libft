#include "libft.h"

char *ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int i;
	char *here;

	i = 0;
	if (!s || !f)
		return (NULL);
	if(!(here = malloc((ft_strlen(s) + 1 ) * sizeof(char))))
		return (NULL);
	while (s[i] != '\0')
	{
		here[i] = f(i, s[i]);
		i++;
	}
	here[i] = '\0';
	return (here);

}

